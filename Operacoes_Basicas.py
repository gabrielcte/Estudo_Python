# Rotina
x = 50
y = 2
print(x + y)
print(x - y)
print(x * y)
print(x / y)

print(x ** y)
print(x // y)
print(x % y)

print(3 // 4)

tem_cafe = True
tem_pao = False

print(not tem_cafe)
print(tem_cafe or tem_pao)
print(tem_cafe and tem_pao)

dolar = 5.3
real = 1

print(dolar > real)
print(dolar < real)
print(dolar == real)
print(dolar >= real)
print(dolar <= real)
print(dolar != real)

# Podemos fazer operações aritméticas simples
a = 2 + 3  # Soma
b = 2 - 3  # Subtração
c = 2 * 3  # Multiplicação
d = 2 / 3  # Divisão
e = 2 // 3 # Divisão inteira
f = 2 ** 3 # Potência
g = 2 % 3  # Resto de divisão

print (a, b, c, d, e, f, g)

# Podemos fazer operações dentro do print

print (a+1, b+1)

#Podemos fazer operações com variáveis não inteiras
